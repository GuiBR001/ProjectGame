def tela_inicio() -> str:
    idx = 0
    while True:
        design_tela_inicio(idx)
        ch = msvcrt.getch()
        if ch in (b"\x00", b"\xe0"):
            ch2 = msvcrt.getch()
            if ch2 == b"H":
                idx = (idx - 1) % len(escolhas)
            elif ch2 == b"P":
                idx = (idx + 1) % len(escolhas)
        elif ch in (b"\r", b"\n"):
            escolha = escolhas[idx]
            if escolha == "Créditos":
                mostrar_creditos()
                continue
            return escolha
        elif ch in (b"1", b"2", b"3", b"4"):
            n = int(ch.decode()) - 1
            if 0 <= n < len(escolhas):
                escolha = escolhas[n]
                if escolha == "Créditos":
                    mostrar_creditos()
                    continue
                return escolha
        else:
            time.sleep(0.00000000000000000000000000001)



